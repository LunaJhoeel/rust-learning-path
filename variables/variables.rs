fn main(){
    // Declare a var
    let a_number;

    // Declare a second var and bind a value
    let a_word = "Ten";

    // Bind a value to first var
    a_number = 10;

    println!(
        "The number is {}",
        a_number
    );
    println!(
        "The word is {}",
        a_word
    );
}